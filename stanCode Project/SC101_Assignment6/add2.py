import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    ans = None
    ans_val = 0                                               # to count the total of the data of ListNode

    while l1 is not None or l2 is not None or ans_val > 0:
        if l1 is not None:
            ans_val += l1.val
            l1 = l1.next

        if l2 is not None:
            ans_val += l2.val
            l2 = l2.next

        if ans is None:                                       # first data
            ans = ListNode(ans_val % 10, None)                # to get its units digit
            ans_val = ans_val // 10                           # to see if it has to be carried

        else:                                                 # append
            new_node = ListNode(ans_val % 10, None)           # to get its units digit
            cur = ans
            while cur.next is not None:
                cur = cur.next
            cur.next = new_node
            ans_val = ans_val // 10                           # to see if it has to be carried

    return ans

